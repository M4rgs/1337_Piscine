int	ft_sqrt(int nb)
{
	int	i;
	int	rzlt;

	i = 1;
	while (i <= nb)
	{
		rzlt = i * i;
		if (rzlt == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
